// Helper function to get books by genre
export const getBooksByGenre = (genre) => {
  if (genre === 'all') return sampleBooks;
  return sampleBooks.filter(book => book.genre === genre);
};

// Helper function to search books
export const searchBooks = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return sampleBooks.filter(book => 
    book.title.toLowerCase().includes(lowercaseQuery) ||
    book.author.toLowerCase().includes(lowercaseQuery) ||
    book.genre.toLowerCase().includes(lowercaseQuery) ||
    book.description.toLowerCase().includes(lowercaseQuery)
  );
};

// Helper function to get book statistics
export const getBookStats = (books = sampleBooks) => {
  const totalBooks = books.length;
  const totalPages = books.reduce((sum, book) => sum + (book.pages || 0), 0);
  const averagePages = totalBooks > 0 ? Math.round(totalPages / totalBooks) : 0;
  
  // Get genre distribution
  const genreCount = {};
  books.forEach(book => {
    if (book.genre) {
      genreCount[book.genre] = (genreCount[book.genre] || 0) + 1;
    }
  });
  
  const mostPopularGenre = Object.keys(genreCount).length > 0 
    ? Object.keys(genreCount).reduce((a, b) => 
        genreCount[a] > genreCount[b] ? a : b
      )
    : '';
  
  // Get language distribution
  const languageCount = {};
  books.forEach(book => {
    if (book.language) {
      languageCount[book.language] = (languageCount[book.language] || 0) + 1;
    }
  });

  return {
    totalBooks,
    totalPages,
    averagePages,
    mostPopularGenre,
    genreDistribution: genreCount,
    languageDistribution: languageCount
  };
};
